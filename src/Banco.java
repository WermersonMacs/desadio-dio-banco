// Classe que representa um Banco
public class Banco {

    // Atributo privado que armazena o nome do banco
    private String nome;

    // Método getter: retorna o nome do banco
    public String getNome() {
        return nome;
    }

    // Método setter: define ou atualiza o nome do banco
    public void setNome(String nome) {
        this.nome = nome;
    }
}
